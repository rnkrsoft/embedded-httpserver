    @Getter
    EmbeddedHttpConnection connection;
    @Getter
    String schema = "";
    @Getter
    String contextPath = "";
    @Getter
    String servletPath = "";

    public EmbeddedServletContext(EmbeddedHttpConnection connection) {
        this.connection = connection;
        //如果http://localhost:8080/news/main/list.jsp
        URI uri = connection.getUri();

        this.schema = uri.getScheme();
        String path = uri.getPath();
        //news
        int contentIndex = -1;
        if (path.length() > 1){
            contentIndex = path.indexOf("/", 1);
        }
        if (contentIndex > -1) {
            //   /news
            this.contextPath = path.substring(0, contentIndex);
            //   /main/list.jsp
            this.servletPath = path.substring(contentIndex);
        } else {
            this.contextPath = path;
            this.servletPath = "";
        }
    }


