    @Override
    public String getMimeType(String file) {
        int lastFilePos = file.lastIndexOf(".");
        String extension = "*";
        //文件资源必须有后缀名
        if (lastFilePos > -1) {
            extension = file.substring(lastFilePos + 1);
            extension = extension.toLowerCase();
        }
        return MimeRegistry.lookupContentType(extension);
    }

